    public GestionarUsuarios() {
        initComponents();
        user = Login.user;
        
        setTitle("Usuarios registrados - Sesi√≥n de " + user);
        setSize(630, 330);
        setResizable(false);
        setLocationRelativeTo(null);
        
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel_Wallpaper.getWidth(),
                jLabel_Wallpaper.getHeight(), Image.SCALE_DEFAULT));
        jLabel_Wallpaper.setIcon(icono);
        this.repaint();
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select usu_id, usu_nombre, usu_user, usu_nivel, usu_estado from usuarios");
            ResultSet rs = pst.executeQuery();
            
            jTable1 = new JTable(model);
            jScrollPane1.setViewportView(jTable1);
            
            model.addColumn(" ");
            model.addColumn("Nombre");
            model.addColumn("Usuario");
            model.addColumn("Permisos");
            model.addColumn("Estado");
            
            while(rs.next()){
                Object[] fila = new Object[5];
                
                for (int i = 0; i < 5; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error al llenar la tabla de usuarios." + e);
            JOptionPane.showMessageDialog(null, "Error al mostrar informacion, contacte al administrador.");
        }
        
        jTable1.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e){
                int fila_point = jTable1.rowAtPoint(e.getPoint());
                int columna_point = 2;
                if (fila_point > -1) {
                    user_update = (String)model.getValueAt(fila_point, columna_point);
                    InformacionUsuario infousu = new InformacionUsuario();
                    infousu.setVisible(true);
                }
            }
        
        });
    }

