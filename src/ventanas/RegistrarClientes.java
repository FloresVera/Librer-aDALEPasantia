    private void jButton_registrarcliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registrarcliActionPerformed
        
        int validacion = 0;
        String nombre, ruc, dv, telefono, direccion;
        
        nombre = txt_nombre.getText().trim();
        ruc = txt_ruc.getText().trim();
        dv = txt_dv.getText().trim();
        telefono = txt_telefono.getText().trim();
        direccion = txt_dire.getText().trim();
        
        if (nombre.equals("")) {
            txt_nombre.setBackground(Color.red);
            validacion ++;
        }
        if (ruc.equals("")) {
            txt_ruc.setBackground(Color.red);
            validacion ++;
        }
        if (dv.equals("")) {
            txt_dv.setBackground(Color.red);
            validacion ++;
        }
        
        if (validacion == 0) {
            
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement(
                        "insert into clientes values (?,?,?,?,?,?,?)");
                
                pst.setInt(1, 0);
                pst.setString(2, nombre);
                pst.setString(3, ruc);
                pst.setString(4, dv);
                pst.setString(5, telefono);
                pst.setString(6, direccion);
                pst.setString(7, user);
                
                pst.executeUpdate();
                cn.close();
                
                Limpiar();
                
                txt_nombre.setBackground(Color.green);
                txt_ruc.setBackground(Color.green);
                txt_dv.setBackground(Color.green);
                txt_telefono.setBackground(Color.green);
                txt_dire.setBackground(Color.green);
                
                JOptionPane.showMessageDialog(null, "Registro exitoso.");
                this.dispose();
                
            } catch (SQLException e) {
                System.err.println("Error en registrar cliente." + e);
                JOptionPane.showMessageDialog(null, "Error en registrar cliente, contacte al administrador.");
            }
            
        } else if(validacion == 1){
            JOptionPane.showMessageDialog(null, "Los campos nombre, ruc y dv son obligatorios.");
        }
        
    }//GEN-LAST:event_jButton_registrarcliActionPerformed

